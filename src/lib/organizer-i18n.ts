const translations: Record<Locale, Record<string, string>> = { en: {}, fr, ar };

export function translateOrganizer(locale: Locale, text: string) {
  return translations[locale][text] ?? text;
}

export function translateOrganizerEnum(locale: Locale, value: string) {
  const label = value
    .toLowerCase()
    .split("_")
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join(" ");

  return translateOrganizer(locale, label);
}
